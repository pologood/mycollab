package com.mycollab.module.file.service

import com.mycollab.db.persistence.service.IService

import java.awt.image.BufferedImage

/**
 * User avatar service which is responsible of uploading and getting avatar
 *
 * @author MyCollab Ltd.
 * @since 1.0
 */
interface UserAvatarService : IService {
    /**
     * @param username
     * @return
     */
    fun uploadDefaultAvatar(username: String): String

    /**
     * Upload user avatar
     *
     * @param image    current user avatar
     * @param username username
     * @param avatarId avatar id which usually be generated by MyCollab as a random
     * stream to avoid overlap or caching old avatar of system.
     * @return
     */
    fun uploadAvatar(image: BufferedImage, username: String, avatarId: String?): String

    /**
     * @param avatarId
     */
    fun removeAvatar(avatarId: String?)
}
